/**
 * @param {number[]} out
 */
export function identity_mat4(out) {
    out[0] = 1;
    out[1] = 0;
    out[2] = 0;
    out[3] = 0;
    out[4] = 0;
    out[5] = 1;
    out[6] = 0;
    out[7] = 0;
    out[8] = 0;
    out[9] = 0;
    out[10] = 1;
    out[11] = 0;
    out[12] = 0;
    out[13] = 0;
    out[14] = 0;
    out[15] = 1;
    return out;
}

/**
 * @param {number[]} out
 * @param {number[]} a
 * @param {number[]} v
 */
export function translate_mat4(out, a, v) {
    const x = v[0],
        y = v[1],
        z = v[2];

    if (a === out) {
        out[12] += (a[0] * x + a[4] * y + a[8] * z);
        out[13] += (a[1] * x + a[5] * y + a[9] * z);
        out[14] += (a[2] * x + a[6] * y + a[10] * z);
        out[15] += (a[3] * x + a[7] * y + a[11] * z);
    } else {
        const a00 = a[0];
        const a01 = a[1];
        const a02 = a[2];
        const a03 = a[3];
        const a10 = a[4];
        const a11 = a[5];
        const a12 = a[6];
        const a13 = a[7];
        const a20 = a[8];
        const a21 = a[9];
        const a22 = a[10];
        const a23 = a[11];
        out[0] = a00;
        out[1] = a01;
        out[2] = a02;
        out[3] = a03;
        out[4] = a10;
        out[5] = a11;
        out[6] = a12;
        out[7] = a13;
        out[8] = a20;
        out[9] = a21;
        out[10] = a22;
        out[11] = a23;
        out[12] = a00 * x + a10 * y + a20 * z + a[12];
        out[13] = a01 * x + a11 * y + a21 * z + a[13];
        out[14] = a02 * x + a12 * y + a22 * z + a[14];
        out[15] = a03 * x + a13 * y + a23 * z + a[15];
    }

    return out;
}

/**
 * @param {number[]} out
 * @param {number[]} a
 * @param {number[]} v
 */
export function scale_mat4(out, a, v) {
    const x = v[0],
        y = v[1],
        z = v[2];
    out[0] = a[0] * x;
    out[1] = a[1] * x;
    out[2] = a[2] * x;
    out[3] = a[3] * x;
    out[4] = a[4] * y;
    out[5] = a[5] * y;
    out[6] = a[6] * y;
    out[7] = a[7] * y;
    out[8] = a[8] * z;
    out[9] = a[9] * z;
    out[10] = a[10] * z;
    out[11] = a[11] * z;
    out[12] = a[12] * x;
    out[13] = a[13] * y;
    out[14] = a[14] * z;
    out[15] = a[15];
    return out;
}
